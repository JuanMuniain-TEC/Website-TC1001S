def square(x, y):
    "Draw square using path at (x, y)."
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

def offset(point):
    "Return offset of point in tiles."
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

def valid(point):
    "Return True if point is valid in tiles."
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

def world():
    "Draw world using path."
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

def move():
    "Move pacman and all ghosts."
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for point, course, target, color in get_ghosts():
        if valid(point + course):
            point.move(course)
        else:
            valid_courses = valid_new_ghost_courses(point, course)
            valid_points = [point + v_course for v_course in valid_courses]
            distances = [distance(target, v_point) for v_point in valid_points]
            plan = valid_courses[distances.index(min(distances))]
            course.x = plan.x
            course.y = plan.y
        up()
        goto(point.x + 10, point.y + 10)
        dot(20, color)

    update()

    for point, course, name in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)


def get_ghosts():  # point, course, target, color
    for point, course, name in ghosts:
        target = vector(0, 0)
        color = "red"
        if name == 'b':
            target = pacman
            color = "red"
        elif name == 'p':
            target = pacman + (4 * aim)
            color = "pink"
        elif name == 'i':
            reference = pacman + (2 * aim)
            blinky_to_ref = reference - ghosts[2][0]
            target = reference + blinky_to_ref
            color = "cyan"
        elif name == 'c':
            target = pacman if distance(pacman, course) <= 8 else vector(0, 0)
            color = "orange"
        yield [point, course, target, color]


def distance(v1, v2):
    return ((v1.x - v2.x)**2 + (v1.y - v2.y)**2)**(1/2)


def valid_new_ghost_courses(point, course):
    options = [
        vector(5, 0),
        vector(-5, 0),
        vector(0, 5),
        vector(0, -5),
    ]
    v_options = []
    for option in options:
        if course != option and valid(point + option):
            v_options.append(option)
    return v_options


def change(x, y):
    "Change pacman aim if valid."
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

